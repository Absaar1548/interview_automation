@router.get(
    "/{interview_id}/questions",
    summary="Get questions for a scheduled interview",
    description="Admin-only. Returns the curated questions for a scheduled interview.",
)
async def get_interview_questions(
    interview_id: str,
    current_admin: User = Depends(get_current_admin),
    session: AsyncSession = Depends(get_db_session),
):
    """Get questions for a scheduled interview."""
    validated_id = validate_uuid(interview_id)
    interview = await InterviewAdminSQLService.get_interview_details(session, validated_id)
    
    if not interview:
        raise HTTPException(status_code=404, detail="Interview not found")
    
    if not interview.curated_questions:
        return {"questions": []}
    
    questions = interview.curated_questions.get('questions', [])
    return {"questions": questions}


@router.put(
    "/{interview_id}/questions",
    summary="Update questions for a scheduled interview",
    description="Admin-only. Updates the curated questions for a scheduled interview.",
)
async def update_interview_questions(
    interview_id: str,
    questions: List[dict] = Body(...),
    current_admin: User = Depends(get_current_admin),
    session: AsyncSession = Depends(get_db_session),
):
    """Update questions for a scheduled interview."""
    validated_id = validate_uuid(interview_id)
    
    async with UnitOfWork(session) as uow:
        interview = await uow.interviews.get_by_id(validated_id, with_for_update=True)
        
        if not interview:
            raise HTTPException(status_code=404, detail="Interview not found")
        
        if interview.status != InterviewStatus.SCHEDULED:
            raise HTTPException(
                status_code=status.HTTP_400_BAD_REQUEST,
                detail="Can only update questions for scheduled interviews"
            )
        
        # Validate questions
        if not questions or len(questions) == 0:
            raise HTTPException(
                status_code=status.HTTP_400_BAD_REQUEST,
                detail="Interview must contain at least one question"
            )
        
        # Reorder questions to ensure sequential order
        for idx, q in enumerate(questions, 1):
            q['order'] = idx
        
        # Update curated_questions
        if interview.curated_questions:
            interview.curated_questions['questions'] = questions
        else:
            interview.curated_questions = {
                "template_id": str(interview.template_id),
                "generated_from": {},
                "generated_at": interview.created_at.isoformat() + "Z",
                "generation_method": "manual_edit",
                "questions": questions
            }
        
        await uow.flush()
        
        return {"message": "Questions updated successfully", "questions": questions}
